void main(List<String> args) {
  final int haftaKuni = 0;

  switch (haftaKuni) {
    case 0:
      print("Yakshanba");
      break;
    case 1:
      print("Dushanba");
    case 2:
      print("Seshanba");
    case 3:
      print("Chorshanba");
  }

  if (haftaKuni == 0) {
    print("Yakshanba");
    
  } else if (haftaKuni == 1) {
    print("dUSANBA");
  } else if (haftaKuni == 2) {
    print("Seshanba");
  }
}
